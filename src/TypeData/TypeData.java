package TypeData;

public class TypeData {
	public static void main (String[]args)
	{
		byte myNumb = 45;
		System.out.println(myNumb);	
		
		short myNums = 1275;
		System.out.println(myNums);	
		
		int myNumi = 25852;
		System.out.println(myNumi);	
		
		long myNuml = 456963123147L;
		System.out.println(myNuml);	
		
	}

}
