package DataType;

public class Datatype {
	public static void main (String[]args)
	{
		byte myNumb = 123;
		System.out.println(myNumb);	
		
		short myNums = 6000;
		System.out.println(myNums);	
		
		int myNumi = 120000;
		System.out.println(myNumi);	
		
		long myNuml = 15100000000L;
		System.out.println(myNuml);	
		
	}

}
